<h1 class="title">Creativity by OSHO (Book Review) — My Personal Note</h1>

<p>This is not a summary. These are the lines that hit me.</p>

<hr>

<blockquote>
<p>Watch yourself and see: 90% of your energy is wasted in activity. And because of this, when the moment for action comes you don't have any energy.</p>
</blockquote>

<p>I decided to focus on three things only. Workout. Logging. Building.</p>
<p>Everything else — skip it.</p>

<hr>

<blockquote>
<p>Relaxation comes to you when there is no urge to activity; the energy is at home, not moving anywhere.</p>
</blockquote>

<p>I used to confuse rest with laziness. Now I understand rest as energy storage.</p>

<hr>

<blockquote>
<p>A creative person always goes on trying the wrong ways. If you always follow the right way to do a thing you will never be creative, because the "right way" means the way discovered by others.</p>
</blockquote>

<p>When I was a new grad, I followed every rule. Every "right way." I did everything correctly — and it made me miserable.</p>
<p>The wrong way is mine. The right way belongs to someone else.</p>

<hr>

<blockquote>
<p>The creative person has to dissolve all shoulds and should nots. He needs freedom and space, vast space; he needs the whole sky and all the stars.</p>
</blockquote>

<p>Same. The moment I stopped following shoulds, I started breathing again.</p>

<hr>

<blockquote>
<p>Money is called currency — it should be a current. The moment you get it, spend it! Don't waste time, because that much time you are preventing the dollar from growing, from becoming more and more.</p>
</blockquote>

<p>Money = currency = it should be a current. If it stops flowing, it's no longer currency. It's just a number.</p>

<hr>

<blockquote>
<p>Don't allow your life to become just a dead ritual.</p>
</blockquote>

<p>This one I re-read three times.</p>
<p>Most of my life was a dead ritual. Wake up, work, sleep, repeat. No color. No surprise. Nothing alive.</p>
<p>I'm building something different now.</p>

